package Class12;

public class Present {
	private String name;
	private double price;
	
	public Present(String nam, double pric) {
		this.name=nam;
		this.price=pric;
	}
	
	
	public String getname() {
		return name;
	}

	public void setname(String nam) {
		this.name = nam;
	}

public double getprice() {
	return price;
}

public void setprice(double pric) {
	this.price = pric;
}

}
